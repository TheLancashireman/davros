#include <kernel/h/dv-kconfig.h>
#include <dv-types.h>

/* dv_strlen() - returns length of a string
*/
int dv_strlen(const char *str)
{
	const char *s = str;
	while ( *s != '\0' )
	{
		s++;
	}
	return (s-str);
}
